/*  Utility: build a date string relative to "today"  */
function daysAgo(n: number, hour = 9, min = 0): string {
  const d = new Date();
  d.setDate(d.getDate() - n);
  d.setHours(hour, min, 0, 0);
  return d.toISOString();
}

